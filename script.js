// - Поставет відповідний оператор в виразах що б вийшов відповідний результат.
// В однакових виразаї не використовувати однакові оператори!!!
  console.log(5 < 6);
  // -> true
  console.log(5 > 6);
  // -> false
  console.log(5 == 6);
  // -> false
  console.log(5 === 6);
   // -> false
  console.log(10 == 10);
  // -> true
  console.log(10 === 10);
  // -> true
  console.log(10 !== 10);
  // -> false
  console.log(10 != 10);
  // -> false
  console.log(10 > 10);
  // -> false
  console.log(123 === '123');
   // -> false
  console.log(123 == '123');
  // -> true
